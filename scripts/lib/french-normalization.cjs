function repairMojibakeText(input) {
  let output = input;
  for (const [search, replacement] of MOJIBAKE_REPLACEMENTS) {
    output = output.split(search).join(replacement);
  }
  return output;
}

function normalizeFrenchText(input, options = {}) {
  const preserveOuterWhitespace = options.preserveOuterWhitespace === true;
  let output = repairMojibakeText(input);

  for (const [pattern, replacement] of WHOLE_TEXT_REPLACEMENTS) {
    output = output.replace(pattern, replacement);
  }

  output = applyCaseAwareAccentWordReplacements(output);
  output = normalizeLeadingImperatives(output);
  output = normalizeFrenchCaseStyle(output);

  for (const [pattern, replacement] of PLACEHOLDER_REPLACEMENTS) {
    output = output.replace(pattern, replacement);
  }

  for (const [search, replacement] of COMMON_REPLACEMENTS) {
    output = output.split(search).join(replacement);
  }

  for (const [pattern, replacement] of ARTIFACT_REPLACEMENTS) {
    output = output.replace(pattern, replacement);
  }

  for (const [pattern, replacement] of APOSTROPHE_PATTERNS) {
    output = output.replace(pattern, replacement);
  }

  output = output.replace(/[ \t]+([,.)\]])/g, "$1");
  output = output.replace(/([(\[])[ \t]+/g, "$1");
  output = output.replace(/([,;:!?])(?![\s<\n\r])/g, "$1 ");
  output = output.replace(/[ \t]{2,}/g, " ");
  output = output.replace(/ \n/g, "\n");
  output = output.replace(/\n{3,}/g, "\n\n");

  const normalizedCore = output.trim();
  if (normalizedCore === "") {
    return input;
  }

  if (!preserveOuterWhitespace) {
    return normalizedCore;
  }

  const leadingWhitespace = String(input).match(/^\s*/u)?.[0] || "";
  const trailingWhitespace = String(input).match(/\s*$/u)?.[0] || "";
  return `${leadingWhitespace}${normalizedCore}${trailingWhitespace}`;
}

